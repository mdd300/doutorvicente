											<div id="accordion-mais-quarenta" class="ui-accordion-header ui-helper-reset ui-state-default ui-corner-all ui-accordion-icons" role="tab" aria-controls="ui-accordion-1-panel-2" aria-selected="false" tabindex="-1"><span class="ui-accordion-header-icon ui-icon ui-icon-triangle-1-e"></span>
												<h3>Pacientes com mais de 40 anos</h3>
											</div>
											<div class="clearfix ui-accordion-content ui-helper-reset ui-widget-content ui-corner-bottom" id="ui-accordion-1-panel-2" aria-labelledby="accordion-mais-quarenta" role="tabpanel" aria-expanded="false" aria-hidden="true" style="display: none;">
												<div class="item_content clearfix">
													<p>
														1) Não congele no estágio de pró-núcleos a menos que mais de 10 pré-zigotos estejam disponíveis e a história prévia apresente embriões com boa morfologia.
													</p>
													<p>
