										<strong>Menstruação</strong><br>
										O sangramento menstrual é causado pela descamação da camada interna que reveste o útero (endométrio), quando não há a implantação do óvulo fertilizado pelo espermatozoide (embrião). Então, outro ciclo se inicia com uma nova ovulação e o útero se prepara novamente para receber ou não um outro óvulo fertilizado (embrião). Se ocorrer a gravidez, o ciclo menstrual será interrompido e reiniciará após o parto.
										<br><br>
										<strong>Menopausa</strong><br>
										Quando a reserva ovariana começa a se esgotar, juntamente com outros fatores envolvidos, a mulher entra no período da menopausa. Mas, antes da menopausa completa, ela passa por ciclos menstruais de aproximadamente 28 dias, que resultam em 10 a 11 ciclos com ovulação (liberação de um óvulo) a cada ano.  Com o avanço da idade, mesmo a mulher apresentando ciclos menstruais regulares, as chances de gravidez já diminuiram significativamente, por conta da baixa qualidade dos óvulos restantes.
