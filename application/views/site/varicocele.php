							</div>
							<p>
								<img src="assets/images/varicocele_4.jpg" alt="" class="img-right">
								Geralmente é feito por meio de exames físicos e de imagem. O paciente deve ser examinado de pé para a inspeção de veias visíveis ou palpáveis no cordão espermático. Além disso, deve-se proceder a investigação de aumento nesse cordão, durante a manobra de Valsalva (aumento da pressão abdominal obstruindo a saída de ar pela boca durante esforço respiratório). É importante ainda, verificar a redução de volume dos testículos, que pode estar associada à varicocele. A severidade da varicocele é medida em graus:
								<br>
								&bull; Varicocele subclínica (não palpável) – Diagnosticada somente por exames de imagem (termografia de fita, doppler e ultrassom com dopplerfluxometria).<br>
								&bull; Grau I (pequena) – Varicocele imperceptível através da pele do escroto e pode ser detectada pela apalpação durante a manobra de Valsalva.<br>
								&bull; Grau II (média) – Também não é visível, porém é palpável em repouso, mesmo sem <br>a manobra de Valsalva;<br>
								&bull; Grau III (grande) – Evidente através da pele da bolsa escrotal.<br>
