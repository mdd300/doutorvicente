									<h3>Como é feita a biópsia aberta (TESE)?</h3>
								</div>
								<p>
									Realiza-se uma incisão de 1,0 cm na pele do escroto e na túnica vaginal. A seguir, é feita uma pequena incisão na túnica albugínea, evitando as veias da região, para obter tecido testicular da porção ântero-medial. O testículo é gentilmente pressionado, para haver a extrusão dos túbulos seminíferos e esse fragmento é excisado com auxílio de tesoura microcirúrgica. O fragmento de testículo é então, depositado em uma placa de petri contendo cerca de 1 ml de meio de cultura (ex. HTF). Em muitos casos, apenas um fragmento é suficiente para fornecer espermatozoides para a ICSI. Entretanto, nos casos de azoospermia não obstrutiva secundária à parada de maturação germinativa, ou à síndrome da presença apenas de células de Sertoli, podem ser necessários três ou mais espécimes.

								</p>

								<p>
									<img src="assets/images/tesa_2.png" alt="TESE" class="img-left">
									Ao final da biópsia, reaproxima-se a túnica albugínea com pontos de vycril ou prolene. O paciente recebe alta após uma hora, em média, podendo retornar ao trabalho no dia seguinte. Então, o material é encaminhado ao laboratório, onde é feita a maceração do tecido embebido no meio de cultura com HEPES. Ele é aspirado e transferido para um tubo, onde é feita uma centrifugação (300g por 5 minutos). O sobrenadante é removido, e o sedimento resultante (pellet) é examinado para verificar a presença de espermatozoides livres. É comum observar-se a presença de espermatozoides exibindo mobilidade muito pequena, geralmente sem progressão, a qual pode aumentar após incubação por 12 horas. Após a ICSI, os espermatozoides remanescentes podem ser congelados para utilização futura.
								</p>


								<div class="logo-title" id="tesa">
									<h3>Como é feita a aspiração Testicular com Agulha Fina (TESA)?</h3>
								</div>
								
								<p>
									<img src="assets/images/tese.png" alt="TESA" class="img-right">
									É um procedimento extremamente simples. O testículo é fixado entre os dedos indicador, polegar e médio. Com a outra mão, introduz-se no testículo uma agulha que é conectada a uma seringa de 20 ml, para facilitar as incursões da agulha no interior do parênquima testicular. 
									<br><br>
									A agulha deve entrar perpendicularmente à pele do escroto, criando uma pressão negativa. Deve-se realizar de quatro a seis incursões da agulha, em diferentes direções. Ao final, a agulha é retirada e o material encaminhado ao laboratório. 
									<br><br>
									Lá, o fluido, é depositado em uma placa de petri contendo meio de cultura e a agulha é lavada com o mesmo meio de cultura. A seguir, é examinada uma gota do material ao microscópio, para checar a presença de espermatozoides. O número de espermatozoides é pequeno no fluido do testículo, especialmente nos portadores de azoospermia não obstrutiva. Por isso, a análise deve ser cuidadosa. 
								</p>

								<p>
									Se houver excesso de hemácias contaminando o material, pode-se utilizar um meio especial para a análise das hemácias. Caso não se encontre espermatozoides, deverá ser realizada uma nova aspiração, no mesmo local, ou do lado contralateral. 
								</p>

								<p>
									Havendo falha da TESA, deve-se mudar a abordagem, prosseguindo para a realização da extração de espermatozoides do testículo (TESE). O sucesso da TESA para obter espermatozoides para a ICSI é mais limitado que a biópsia aberta, principalmente nos casos de azoospermia não obstrutiva. Com esse método, é raro haver material excedente para o congelamento. A incidência de complicações como hematomas intratesticulares é pequena, porém, o risco existe e não deve ser desprezado. Graças à simplicidade do procedimento, a TESA ainda é considerada a primeira opção para obter espermatozoides do testículo, passando para a TESE em caso de falha.
								</p>

