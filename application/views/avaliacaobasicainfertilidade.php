										</div>
											Análise seminal completa (espermograma e testes funcionais) - Esse exame é muito importante e a maioria dos pacientes que buscam o tratamento para a infertilidade já realizou pelo menos um espermograma. 
											<br><br>
											É importante lembrar que o exame deve ser um dos primeiros a ser realizado no processo de avaliação. Se o teste mostrar alterações graves, a investigação do fator feminino poderá ser modificada e o tratamento pode ser direcionado para o problema. 
											<br><br>
											Cerca de 25% de toda infertilidade é causada por alterações espermáticas e 50% dos casos de infertilidade, o fator masculino está presente como causa principal ou associado a outro fator feminino.
											<br>
											<div class="dropcap_label logo-title" style="margin-top: 30px;">
												<h3>Outros exames complementares</h3>
											</div>
												<strong>Histerossalpingografia (HSG)</strong><br>
												Verificamos que a maioria das mulheres em tratamento já realizou esse exame alguma vez. 
												<br><br>

												O exame tem o objetivo de avaliar a permeabilidade e a anatomia das tubas uterinas (trompas). Cerca de 25% de toda infertilidade é causada pelo fator tubário e, portanto, ele tem sua importância. Porém, é um exame limitante, já que não diferencia as más formações uterinas ou aponta a existência de doença peritoneal. 
												<br><br>

												<strong>Histeroscopia</strong><br>
												Exame que permite a visualização direta da cavidade uterina, avaliando o endométrio (pólipos, endometrites, sinéquias) e a presença de miomas submucosos. Ele complementa exames como a histerossalpingografia e a histerossonografia. 
												<br><br>

												<strong>Exames de pouca utilidade em uma primeira investigação</strong><br>
												- Avaliação do muco cervical <br>
												- Biópsia endometrial <br>
												- Culturas cervicais <br>
												- Pesquisa de anticorpos antiespermatozoide <br><br>

												<strong>Testes imunológicos</strong> - Não têm provado valor algum em pacientes com infertilidade, a não ser naquelas com história de 2 ou mais abortos. 
												<br><br>

												<strong>Laparoscopia</strong> - É um procedimento cirúrgico e não deve ser realizado até que se tenha efetivado a avaliação básica do casal. Em alguns casos, a laparoscopia pode ser indicada para procurar aderências pélvicas ou endometriose.
