								<p>
									<img src="assets/images/pcr_dna.png" alt="PCR" class="img-left">A técnica da PCR revolucionou a análise do DNA. O procedimento envolve repetidas amplificações do DNA para obter cópias adequadas para análise. O uso dessa técnica permite detectar mutações em genes, corpúsculos polares ou blastômeros. 
									<br><br>
									O DNA amplificado pode ser submetido a análise de mutações através de vários métodos, cuja seleção é determinada pelas características da mutação investigada.
