														<h2 style="color: #671b68;">Protocolo Curto</h2>
														<p>Nesse protocolo a fase de bloqueio prévio não ocorre. O bloqueio se dá durante a fase de estimulação, com o uso de antagonistas do Gn-RH ( Cetrotide ou Orgalutran ).</p>
														<p>Fase de estimulação:</p>
														<p>9 a 12 dias. Em alguns casos é possível que demore mais mais tempo para que os folículos atinjam o tamanho ideal. Medicações utilizadas nesta fase: FSH recombinante (GONAL – F) e no final o HCG (Ovidrel ) que será dado com o objetivo de fazer a maturação final dos óvulos. Trinta e cinco horas após o Ovidrel, a paciente deverá realizar a aspiração dos óvulos.</p>
														<p>Após a aspiração, outras medicações de suporte e preparo do útero para a transferência dos embriões serão iniciadas. A transferência dos embriões poderá ocorrer 48 horas, 72 horas ou até 5 dias após a aspiração dos óvulos. Isto será decidido pelos biólogos e pelo corpo clínico, após análise individual dos casos</p>
														<p>A duração desse desse tipo tratamento é de aproximadamente  15 dias.</p>
