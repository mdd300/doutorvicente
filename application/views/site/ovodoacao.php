											<h2>Ovodoação</h2>
											<div class="dropcap">
												<div class="dropcap_label logo-title">
													<h3>Histórico</h3>
												</div>
													A primeira gravidez resultante de uma ovodoação ocorreu em 1984. A paciente apresentava falência ovariana prematura, ou seja, entrou precocemente na menopausa. Em 1989, essa técnica passou a ser aplicada em mulheres com idade avançada, que já se encontravam na menopausa. No Brasil, o procedimento é bastante estabelecido, ético e contemplado pelas Normas do Conselho Federal de Medicina.
													Para isso, as clínicas de Reprodução Humana devem obedecer algumas condições, como: doação sem caráter comercial, manter o anonimato das identidades das mulheres envolvidas (doadoras e receptoras).
													<br><br>
													Veja a <a href="#nova_resolucao" class="fancybox open_lightbox">nova resolução do Conselho Federal de Medicina</a> (Maio/2017) sobre a doação de óvulos.
													<br><br>
													O tratamento com doação de óvulos consiste em um ciclo de Fertilização In Vitro e uma doadora de óvulos. Uma parte desses óvulos será doada para a receptora, que já não tem mais a capacidade de produzir óvulos, em quantidade e qualidade satisfatórias. Ela terá o útero preparado com hormônios antes de receber o embrião, usando medicamentos para manter a gestação no seu primeiro trimestre (até 11 semanas). Eles são mantidos até o terceiro mês de gestação, quando a placenta passa a ser a responsável por essa manutenção. <br>
