													As principais causas de falha de fertilização na FIV convencional são: número extremamente baixo de espermatozoides, alterações de mobilidade e morfologia.  <br>

													Vários procedimentos de fertilização assistida baseados na micromanipulação de espermatozoides e óvulos foram desenvolvidos para tentar solucionar esses problemas. A evolução dessas técnicas começou com a técnica da PZD (Partial Zona Dissection = Dissecção Parcial da Zona Pelúcida), seguido pela SUZI (Subzonal Insemination = Inseminação de espermatozóides no espaço peri-vitelino) e, finalmente, pela técnica de ICSI (Intracytoplasmic Sperm Injection = Injeção Intra-Citoplasmática de Espermatozóides). <br><br>

													As duas primeiras técnicas foram desenvolvidas para ultrapassar a barreira da zona pelúcida. O PZD faz a ruptura mecânica da zona pelúcida do oócito e, logo após, a inseminação dos espermatozoides, que terão livre acesso ao espaço peri-vitelino.  <br><br>

													Na técnica SUZI, vários espermatozoides móveis são liberados no espaço peri-vitelino, por meio de uma pipeta de injeção. A técnica de ICSI é mais invasiva, porque um único espermatozoide é imobilizado e injetado dentro do ooplasma, cruzando não somente a zona pelúcida, mas a oolema.  <br><br>
													Em 1992, o grupo de Van Steirteghem, relatou as primeiras gestações humanas e nascimentos após a transferência de embriões produzidos por este novo procedimento. O uso da PZD tornou-se controverso e foi abandonado, pois as taxas de fertilização após a ICSI eram significativamente melhores que após a SUZI.  <br><br>

													Por outro lado, a ICSI resultou na produção de mais embriões com melhores taxas de implantação. A consequência desse fato, é que a ICSI tem sido usada em todo o mundo para tratar a infertilidade masculina resultante de oligoastenospermia severa, teratozoospermia, e mesmo, azoospermia no ejaculado. <br><br>
