										As obstruções nas trompas, também chamadas de tubas, são provavelmente as campeãs na incidência de infertilidade feminina. Podem ocorrer como sequela de infecções e endometriose ou serem provocadas intencionalmente por laqueadura. As trompas desempenham um papel decisivo na fecundação, pois este é o local onde se dá o encontro do óvulo com o espermatozoide. Após a fertilização, as trompas têm também a função de conduzir o embrião recém-formado para o útero. Portanto, não pode haver obstáculo no caminho, tanto para a chegada do espermatozoide até o óvulo quanto para a jornada do embrião até a "casa" que hospedará seu desenvolvimento durante a gestação.
