													<h3>IV - DOAÇÃO DE GAMETAS OU EMBRIÕES</h3>
													<p>
														<strong>1</strong> - A doação nunca terá caráter lucrativo ou comercial.  <br><br>
														<strong>2</strong> - Os doadores não devem conhecer a identidade dos receptores e vice-versa.  <br><br>
														<strong>3</strong> - A idade limite para a doação de gametas é de 35 anos para a mulher e 50 anos para o homem.  <br><br>
														<strong>4</strong> - Obrigatoriamente será mantido o sigilo sobre a identidade dos doadores de gametas e embriões, bem como dos receptores. Em situações especiais, as informações sobre doadores, por motivação médica, podem ser fornecidas exclusivamente para médicos, resguardando-se a identidade civil do doador.  <br><br>
														<strong>5</strong> - As clínicas, centros ou serviços que empregam a doação devem manter, de forma permanente, um registro de dados clínicos de caráter geral, características fenotípicas e uma amostra de material celular dos doadores, de acordo com a legislação vigente.  <br><br>
														<strong>6</strong> - Na região de localização da unidade, o registro dos nascimentos evitará que um(a) doador(a) tenha produzido mais que duas gestações de crianças de sexos diferentes, numa área de um milhão de habitantes.  <br><br>
														<strong>7</strong> - A escolha dos doadores é de responsabilidade da unidade. Dentro do possível, deverá garantir que o doador tenha a maior semelhança fenotípica e imunológica e a máxima possibilidade de compatibilidade com a receptora.  <br><br>
														<strong>8</strong> - Não será permitido ao médico responsável pelas clínicas, unidades ou serviços, nem aos integrantes da equipe multidisciplinar que nelas prestam serviços, participarem como doadores nos programas de RA.   <br><br>
														<strong>9</strong> - É permitida a doação voluntária de gametas, bem como a situação identificada como doação compartilhada de oócitos em RA, onde doadora e receptora, participando como portadoras de problemas de reprodução, compartilham tanto do material biológico quanto dos custos financeiros que envolvem o procedimento de RA. A doadora tem preferência sobre o material biológico que será produzido. <br><br>
