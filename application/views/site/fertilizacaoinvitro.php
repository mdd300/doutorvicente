											<div class="clearfix ui-accordion-content ui-helper-reset ui-widget-content ui-corner-bottom" id="ui-accordion-1-panel-0" aria-labelledby="accordion-primary-health-care" role="tabpanel" aria-expanded="false" aria-hidden="false">
												<div class="item_content clearfix">
													<p>
														Os ovários são estimulados com medicações hormonais para produzir o maior número de óvulos possível. Quanto maior o número de óvulos, maior será a chance de formar embriões de qualidade, aumentando as chances de gravidez. Há diversas maneiras de estimular os ovários (protocolos de estimulação). Aqui, na Clínica, procuramos usar as medicações mais puras do mercado, já que elas formam óvulos de melhor qualidade. A maioria dos casos da Clínica segue uma mesma linha de estimulação ovariana. Ela pode ser dividida em duas fases: inicial ou de bloqueio e de estimulação.
													</p>
													<p>
														Fase inicial ou de bloqueio - A paciente usa uma medicação que bloqueia o ciclo menstrual natural, por isso o tratamento pode começar em qualquer fase do ciclo menstrual. O bloqueio é necessário para o controle total do tratamento, além de fazer que todos os folículos (estruturas do ovário onde estão os óvulos) cresçam juntos, evitando a ovulação antes da hora.
													</p>

													<div class="logo-title">
														<h3>Fase de estimulação:</h3>
													</div>
													<p>
														<img src="assets/images/estimulacao_ovariana.png" alt="" class="img-left">
														A paciente usa uma medicação (gonadotrofinas) que age diretamente nos ovários, promovendo o crescimento dos folículos e a formação dos óvulos, até chegarem ao ponto de maturidade para serem aspirados. Em certo momento desta fase, a paciente deve fazer controles diários, ou em dias alternados, com ultrassonografia e dosagens hormonais (estradiol e progesterona) ministrados na Clínica, para saber o melhor momento de aspirar os óvulos.
													</p>
													<p>
														<img src="assets/images/estimulacao_ovariana2.png" alt="" class="img-left">
														A avaliação do desenvolvimento folicular é feita pela medição do diâmetro médio do folículo em ultrassom transvaginal. Quando pelo menos dois folículos atingem um diâmetro de > 18-20 mm administra-se uma nova medicação, com hora marcada, chamada hCG (gonadotrofina coriônica humana), para após 35 horas realizar a coleta dos óvulos.
													</p>
													
													<div class="logo-title">
														<h3>Duração do tratamento</h3>
													</div>
													<p>
														Existem dois tipos de protocolos: o longo e o curto, que serão indicados conforme o caso.
													</p>
													<p>
														Confira a duração de cada fase até a aspiração dos óvulos e transferência dos embriões:
													</p>
													<p>
														<h2 style="color: #671b68;">Protocolo Longo</h2>
													</p>
													<ul>
														<li>&bull; Fase de bloqueio - 14 dias</li>
														<li>&bull; Medicações empregadas nessa fase: Gonapeptyl Daily ou Lupron, poderá ser iniciada em qualquer dia do ciclo menstrual.</li>
														<li>&bull; Realização de exames de ultrassom e dosagem de estradiol para verificar a possibilidade de começar a fase de estimulação propriamente dita.</li>
														<li>Fase de estimulação - 9 a 12 dias</li>
													</ul>
													<p>
														Em alguns casos pode demorar mais tempo para que os folículos atinjam o tamanho ideal. Medicações empregadas nessa fase: FSH recombinante (GONAL – F) e no final o HCG (Ovidrel), ministrado para a maturação final dos óvulos, 35 horas após o Ovidrel, deve ser feita a aspiração dos óvulos.
													</p>
													<p>
														Após a aspiração, são iniciadas outras medicações de suporte e preparo do útero. A transferência dos embriões pode ocorrer em 48, 72 horas ou até cinco dias após a aspiração dos óvulos. A decisão cabe aos biólogos e corpo clínico, após analisar o caso.
